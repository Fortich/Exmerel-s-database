    public formatoVenta(String nombre, Connection c) {
        this.c = c;
        nomCosa = nombre;
        initComponents();
        labelTitulo.setText("Crear " + nombre);
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Producto");
        model.addColumn("Cantidad");
        model.addColumn("Precio");
        try {
            Statement s = c.createStatement();
            estaEs.setModel(model);
            ResultSet re;
            listaProducto.removeAllItems();
            listaNIT.removeAllItems();
            listaVendedor.removeAllItems();
            if (nombre.equals("compra")) {
                jLabel2.setText("Administrador:");
                re = s.executeQuery("select * from vw_proveedores;");
                while (re.next()) {
                    listaNIT.addItem(re.getString(1));
                }
                re = s.executeQuery("select * from vw_administrativos;");
                while (re.next()) {
                    listaVendedor.addItem(re.getString(1));
                }
            } else {
                jLabel2.setText("Vendedor:");
                re = s.executeQuery("select * from vw_Clientes;");
                while (re.next()) {
                    listaNIT.addItem(re.getString(1));
                }
                re = s.executeQuery("select * from vw_vendedores;");
                while (re.next()) {
                    listaVendedor.addItem(re.getString(1));
                }
            }
            re = s.executeQuery("select * from vw_productos;");
            while (re.next()) {
                listaProducto.addItem(re.getString(1));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

